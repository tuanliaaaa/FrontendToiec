function startExam() {
    const countdownDisplay = document.getElementById("countdown-display");
    let totalSeconds = 7199;

    function formatTime(seconds) {
        const hours = Math.floor(seconds / 3600);
        const minutes = Math.floor((seconds % 3600) / 60);
        const secs = seconds % 60;
        return `${hours.toString().padStart(2, "0")}:${minutes
            .toString()
            .padStart(2, "0")}:${secs.toString().padStart(2, "0")}`;
    }

    clearInterval(window.countdownInterval);

    window.countdownInterval = setInterval(() => {
        if (totalSeconds > 0) {
            countdownDisplay.textContent = formatTime(totalSeconds);
            totalSeconds--;
        } else {
            showPageSuccess()
            clearInterval(window.countdownInterval);
        }
    }, 1000);

    const showBoard = document.getElementById("board-container");
    let showBoardHtml = '',
        countPart = 1;
    for (let i = 1; i < 201; i++) {
        if (i > 100) {
            if (i === 1 || i === 7 || i === 32 || i === 71 || i === 101 || i === 131 || i === 147) {
                showBoardHtml += '<h4>Part ' + countPart + '</h4><div class="board-part"><span class="question action question-'+i+'" data-index="' + i + '">' + i + '</span>';
                countPart++;
            } else if (i === 6 || i === 31 || i === 70 || i === 100 || i === 130 || i === 146 || i === 200) {
                showBoardHtml += '<span class="question action question-'+i+'" data-index="' + i + '">' + i + '</span></div>';
            } else {
                showBoardHtml += '<span class="question action question-'+i+'" data-index="' + i + '">' + i + '</span>';
            }
        } else {
            if (i === 1 || i === 7 || i === 32 || i === 71 || i === 101 || i === 131 || i === 147) {
                showBoardHtml += '<h4>Part ' + countPart + '</h4><div class="board-part"><span class="question question-'+i+'" data-index="' + i + '">' + i + '</span>';
                countPart++;
            } else if (i === 6 || i === 31 || i === 70 || i === 100 || i === 130 || i === 146 || i === 200) {
                showBoardHtml += '<span class="question question-'+i+'" data-index="' + i + '">' + i + '</span></div>';
            } else {
                showBoardHtml += '<span class="question question-'+i+'" data-index="' + i + '">' + i + '</span>';
            }
        }
    }
    showBoard.innerHTML += showBoardHtml;
}

